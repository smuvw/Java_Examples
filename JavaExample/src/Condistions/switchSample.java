package Condistions;

public class switchSample {

	public static void main(String[] args) {
       
		char grade='C';
				
		switch(grade){
		
		case 'A':
			System.out.println("First Grade");
		
			break;
		case 'B':
			System.out.println("Second Grade");
			break;
		case 'C':
			System.out.println("Third Grade");
			break;
		case 'D':
			System.out.println("Fourth Grade");
			break;
			
		
		
		}
		
	}

}

package Operators;

public class Relational {
	//<,>,<=,>=.==,!=(not)

		public static void main(String[] args) {
			
			int a=10;
			int b=20;
			
			System.out.println("a==b equal  " + (a==b));
			System.out.println("a!=b equal  " + (a!=b));
			System.out.println("a<b equal  " + (a<b));
			System.out.println("a>b equal  " + (a>b));
			System.out.println("a<=b equal  " + (a<=b));
			System.out.println("a>=b equal  " + (a>=b));
			System.out.println("welcome to " + "Java Training ");
			
			

		}
}

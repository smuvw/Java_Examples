package loops;

public class forLoopExample1 {
	
	public static void main(String[] args) {
		
		
		
		  char Grade;
		  
		  Grade='D';

		  
		  if (Grade=='A') {
			  System.out.println("A grade ");
		  } else if (Grade=='B') {
			  System.out.println("B grade ");
		  }
		  else if (Grade=='C') {
			  System.out.println("C grade ");
		  }
		  else  {
			  System.out.println("D grade ");
		  }

			}

}

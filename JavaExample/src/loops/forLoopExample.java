package loops;


/*  FOR Loop syntax:
 for( <initialization> ; <condition> ; <statement> ){
	 
    <Block of statements>;

}*/

public class forLoopExample {

	public static void main(String[] args) {
		
		for(int x=1; x<10;x++) {
			
			System.out.println(x);
		}
		
		for(int x=20; x>10;x--) {
			
			System.out.println(x);
		}
		
		
	}

}
